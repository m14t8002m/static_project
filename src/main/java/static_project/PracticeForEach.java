package static_project;

public class PracticeForEach {
	public static void main(String[] args) {
		String list[] = {
				"佐藤",
				"鈴木",
				"高橋",
				"田中",
				"伊藤",
				"渡辺",
				"山本",
				"中村",
				"小林",
				"加藤",
				"吉田",
				"山田"
		};

		for (int i = 0; i < list.length; i++) {
			System.out.println(list[i]);
		}
	}
}
